<template>
  <li class="list-group-item media" @click="onVideoSelect">
    <img class="mr-3" :src="thumbnailUrl" alt="Video Thumbnail Image">
    <div class="media-body">{{ title }}</div>
  </li>
</template>

<script>
export default {
  name: "VideoListItem",
  props: {
    video: Object
  },
  computed: {
    thumbnailUrl() {
      return this.video.snippet.thumbnails.default.url;
    },
    title() {
      return this.video.snippet.title;
    }
  },
  methods: {
    onVideoSelect() {
      this.$emit("videoSelect", this.video);
    }
  } 
}
</script>

<style scoped>
  li {
    cursor: pointer;
    display: flex;
  }

  li:hover {
    background: #eee;
  }
</style>

